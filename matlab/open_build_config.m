function build_config = open_build_config()

build_name = 'autogenerated_optimizer';
optimization_engine_root_path = matlab_open_root();
icasadi_path = fullfile(optimization_engine_root_path, 'icasadi');
build_path = fullfile(optimization_engine_root_path, 'build');

udp_interface_config = struct('bind_address', '127.0.0.1', ...
    'port', 3248);

authors = cell(2,1);
authors{1} = 'P. Sopasakis <p.sopasakis@example.com>';
authors{2} = 'E. Fresk <e.fresk@example.com>';
build_config = struct(...
    'build_name', build_name, ...    
    'version', '0.1.0', ...
    'build_mode', 'debug', ...
    'license', 'unspecified', ...
    'target', 'default', ...
    'optimization_engine_path', optimization_engine_root_path, ...
    'icasadi_path', icasadi_path, ...
    'build_path', build_path, ...
    'udp_interface', udp_interface_config, ...
    'communication_buffer_size', 256);

build_config.authors = authors;

build_config.solver = struct('lbfgs_mem', 10, 'max_iters', 500, 'tolerance', 1e-5);